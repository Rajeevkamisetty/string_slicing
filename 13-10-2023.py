# string slicing

name = "rajeev"
print(name[0:4])
print(name[-5:-1])

# reverse a string
name = "rajeev"
print(name[::-1])

# reverse a list
names = ["rajeev", "sai", "pretham", "basha"]
print(names[::-1])

# reverse a list without slicing
names = ["rajeev", "sai", "pretham", "basha"]
names.reverse()
print(names)
